class JavaExample {
    
    static int num;
    static String str;
    static{
        
        num=97;
        str="Static Keyword in Java";
    
    }
    public static void main (String[] args)
    {
        
        System.out.println("value of num= "+num);
        System.out.println("value of str= "+str);
    
    }
}
